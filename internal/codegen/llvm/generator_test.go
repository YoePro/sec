func TestGenerateMinimalMainWithIf(t *testing.T) {
	input := `
module main

fn main() int {
	if true {
		return 0
	}

	return 1
}
`

	program := parseAndAnalyze(t, input)
	got, err := Generate(program)
	if err != nil {
		t.Fatalf("Generate returned error: %v", err)
	}

	expected := `; generated by sec
target triple = "` + hostTargetTriple() + `"

define i32 @main() {
entry:
  br i1 true, label %if.then.0, label %if.end.1

if.then.0:
  ret i32 0

if.end.1:
  ret i32 1
}
`

	if strings.TrimSpace(got) != strings.TrimSpace(expected) {
		t.Fatalf("wrong LLVM IR.\nwant:\n%s\n\ngot:\n%s", expected, got)
	}
}

