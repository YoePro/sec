func TestTargetTripleFromHostParts(t *testing.T) {
	tests := []struct {
		goos     string
		goarch   string
		expected string
	}{
		{goos: "linux", goarch: "amd64", expected: "x86_64-pc-linux-gnu"},
		{goos: "linux", goarch: "arm64", expected: "aarch64-pc-linux-gnu"},
		{goos: "darwin", goarch: "amd64", expected: "x86_64-apple-darwin"},
		{goos: "darwin", goarch: "arm64", expected: "aarch64-apple-darwin"},
		{goos: "windows", goarch: "amd64", expected: "x86_64-pc-windows-msvc"},
		{goos: "freebsd", goarch: "amd64", expected: "x86_64-unknown-freebsd"},
	}

	for _, tt := range tests {
		got := targetTriple(tt.goos, tt.goarch)
		if got != tt.expected {
			t.Fatalf("targetTriple(%q, %q) = %q, want %q", tt.goos, tt.goarch, got, tt.expected)
		}
	}
}

